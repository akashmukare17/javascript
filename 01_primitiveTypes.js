



//primitive type

//1. Number
var age = 22;
var weight = 55;
var height = 5.8;
var specNumber = 0.5;
console.log(age, weight, height, specNumber);
var typeOfAge = typeof age;
console.log(typeOfAge);
console.log(typeof specNumber);

console.log("Age:", age);
console.log("Weight:", weight);
console.log("Height:", height); 
console.log('Spec Number:', specNumber);

//2. Boolean -->true or false
var isMarried = true;
var isSoftwareEngineer = true;
var isBachlor = false;
console.log(isMarried);
console.log(typeof isBachlor);

//3. String --> Sequence of character enclosed in '' or ""
var firstName = "Bill";
var position = 'Microsoft Co-founder';
var city = ""
console.log(typeof city);
console.log(typeof firstName);
console.log('=====');
console.log(typeof position);
console.log("Type of variable --> firstName:", typeof firstName);
console.log("Type of variable --> position:", typeof position);

//2. Special Data type
